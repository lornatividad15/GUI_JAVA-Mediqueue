    public UpdatePatientDashboard(int patientId) {
        initComponents();
        String path = "/Images/1766.png";
        ImageIcon icon = new ImageIcon(getClass().getResource(path));
        Image scaledImage = icon.getImage().getScaledInstance(exitICON.getWidth(), exitICON.getHeight(), Image.SCALE_SMOOTH);
        exitICON.setIcon(new ImageIcon(scaledImage));
        // Set ID and arrival time fields as non-editable
        patientID_updateTF.setEditable(false);
        patientTime_updateTF.setEditable(false);
        // Load patient info from DB
        try {
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/mediqueue", "root", "");
            String sql = "SELECT * FROM patient WHERE id = ?";
            PreparedStatement stmt = conn.prepareStatement(sql);
            stmt.setInt(1, patientId);
            ResultSet rs = stmt.executeQuery();
            if (rs.next()) {
                patientID_updateTF.setText(String.valueOf(rs.getInt("id")));
                patientName_updateTF.setText(rs.getString("name"));
                patientAge_updateTF.setText(String.valueOf(rs.getInt("age")));
                String gender = rs.getString("gender");
                if ("Male".equalsIgnoreCase(gender)) {
                    male_updateRB.setSelected(true);
                } else if ("Female".equalsIgnoreCase(gender)) {
                    female_updateRB.setSelected(true);
                } else {
                    buttonGroup1.clearSelection();
                }
                patientTime_updateTF.setText(rs.getString("arrival_time"));
                patientSymptoms_updateTF.setText(rs.getString("symptoms"));
                priority_updateCoB.setSelectedItem(rs.getString("priority"));
            } else {
                JOptionPane.showMessageDialog(this, "Patient ID not found.");
                this.dispose();
            }
            rs.close();
            stmt.close();
            conn.close();
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Database error occurred.");
            this.dispose();
        }
    }

