    private void update_patientBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_update_patientBTNActionPerformed
        String idText = patientID_updateTF.getText().trim();
        String name = patientName_updateTF.getText().trim();
        String ageText = patientAge_updateTF.getText().trim();
        String gender = male_updateRB.isSelected() ? "Male" : (female_updateRB.isSelected() ? "Female" : "");
        String symptoms = patientSymptoms_updateTF.getText().trim();
        String priority = (String) priority_updateCoB.getSelectedItem();
        // Do not update ID or arrival_time
        if (name.equals("Name") || name.isEmpty() ||
            ageText.equals("Age") || ageText.isEmpty() ||
            gender.isEmpty() ||
            symptoms.equals("Symptoms") || symptoms.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please fill in all fields.");
            return;
        }
        try {
            int id = Integer.parseInt(idText);
            int age = Integer.parseInt(ageText);
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/mediqueue", "root", "");
            String sql = "UPDATE patient SET name=?, age=?, gender=?, symptoms=?, priority=? WHERE id=?";
            PreparedStatement stmt = conn.prepareStatement(sql);
            stmt.setString(1, name);
            stmt.setInt(2, age);
            stmt.setString(3, gender);
            stmt.setString(4, symptoms);
            stmt.setString(5, priority);
            stmt.setInt(6, id);
            int result = stmt.executeUpdate();
            if (result > 0) {
                JOptionPane.showMessageDialog(this, "Patient information updated successfully.");
            } else {
                JOptionPane.showMessageDialog(this, "Update failed. Patient ID not found.");
            }
            stmt.close();
            conn.close();
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Age must be a number.");
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Database error occurred.");
        }
    }//GEN-LAST:event_update_patientBTNActionPerformed

